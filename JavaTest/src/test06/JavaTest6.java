public class JavaTest6 {

	public static void main(String[] args) {
		
		int num1 = 0;
		int num2 = 1;
		
		int result = num1++;
		System.out.println("result : "+result);
		
		result = ++num2;
		System.out.println("result : "+result);
		
		result = num2--;
		System.out.println("result : "+result);
		
		result = --num1;
		System.out.println("result : "+result);
		
		

	}

}
